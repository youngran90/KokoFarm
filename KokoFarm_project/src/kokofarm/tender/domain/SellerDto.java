package kokofarm.tender.domain;

public class SellerDto {
	private String seller_no;
	private String seller_id;
	private String seller_name;
	private String seller_registerno;
	private String seller_phoneno;
	private String seller_address;
	private String seller_date;
	private String seller_grade;
	
	
	public String getSeller_no() {
		return seller_no;
	}
	public void setSeller_no(String seller_no) {
		this.seller_no = seller_no;
	}
	public String getSeller_id() {
		return seller_id;
	}
	public void setSeller_id(String seller_id) {
		this.seller_id = seller_id;
	}
	public String getSeller_name() {
		return seller_name;
	}
	public void setSeller_name(String seller_name) {
		this.seller_name = seller_name;
	}
	public String getSeller_registerno() {
		return seller_registerno;
	}
	public void setSeller_registerno(String seller_registerno) {
		this.seller_registerno = seller_registerno;
	}
	public String getSeller_phoneno() {
		return seller_phoneno;
	}
	public void setSeller_phoneno(String seller_phoneno) {
		this.seller_phoneno = seller_phoneno;
	}
	public String getSeller_address() {
		return seller_address;
	}
	public void setSeller_address(String seller_address) {
		this.seller_address = seller_address;
	}
	public String getSeller_date() {
		return seller_date;
	}
	public void setSeller_date(String seller_date) {
		this.seller_date = seller_date;
	}
	public String getSeller_grade() {
		return seller_grade;
	}
	public void setSeller_grade(String seller_grade) {
		this.seller_grade = seller_grade;
	}
	
}
